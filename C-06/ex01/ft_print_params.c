#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char	letter;
	int		i;

	i = 0;
	letter = str[0];
	while (str[i] != '\0')
	{
		ft_putchar(letter);
		i++;
		letter = str[i];
	}
}

int	main(int argc, char **argv)
{
	int		i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		ft_putchar('\n');
		i++;
	}
}
