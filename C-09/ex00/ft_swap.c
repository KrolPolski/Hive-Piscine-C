void	ft_swap(int *a, int *b)
{
	int	old_a;
	int	old_b;
	int	new_a;
	int	new_b;

	old_a = *a;
	old_b = *b;
	new_a = old_b;
	new_b = old_a;
	*a = old_b;
	*b = old_a;
}
